// 10. Напишите программу (простейший консольный калькулятор), которая принимает
//от пользователя два целых числа и поочередно выводит результаты всех простейших
//математических (сложение, вычитание, умножение, деление, остаток от деления),
//всех битовых операций (И, ИЛИ, исключающее ИЛИ) для этих чисел, а также адреса этих чисел,
//инкремент и декремент указателей на эти числа.

#include <iostream>
using namespace std;

int main() {
    int a, b;
    cout << "Введите два целых числа: ";
    cin >> a >> b;

    cout << "\nМатематические операции:" << endl;
    cout << a << " + " << b << " = " << a + b << endl;
    cout << a << " - " << b << " = " << a - b << endl;
    cout << a << " * " << b << " = " << a * b << endl;
    cout << a << " / " << b << " = " << a / b << endl;
    cout << a << " % " << b << " = " << a % b << endl;

    cout << "\nБитовые операции:" << endl;
    cout << a << " & " << b << " = " << (a & b) << endl;
    cout << a << " | " << b << " = " << (a | b) << endl;
    cout << a << " ^ " << b << " = " << (a ^ b) << endl;

    cout << "\nАдреса переменных:" << endl;
    cout << "Адрес a: " << &a << endl;
    cout << "Адрес b: " << &b << endl;

    int* ptrA = &a;
    int* ptrB = &b;

    cout << "\nИнкремент указателей:" << endl;
    cout << "Указатель на a: " << ptrA << endl;
    cout << "Указатель на a после инкремента: " << ++ptrA << endl;

    cout << "Указатель на b: " << ptrB << endl;
    cout << "Указатель на b после декремента: " << --ptrB << endl;

    return 0;
}
